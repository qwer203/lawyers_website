// 2. Отрисовка
const quizContainer = document.getElementById('quiz');

questions.forEach((q, index) => {
  // Заголовок вопроса
  let html = `<h3>${index + 1}. ${q.text}</h3>`;

  // Варианты ответов
  q.options.forEach((opt, optIndex) => {
    html += `
        <div>
            <input type="radio" name="q${index}" value="${optIndex}" id="q${index}_opt${optIndex}">
            <label for="q${index}_opt${optIndex}">${opt}</label>
        </div>
    `;
  });

  html += "<hr>"; // Разделитель между вопросами
  quizContainer.innerHTML += html;
});
// 3. Проверка (ИЗМЕНЕННАЯ ЧАСТЬ)
function checkAnswers() {
  let score = 0;

  questions.forEach((q, index) => {
    const selected = document.querySelector(`input[name="q${index}"]:checked`);
    if (selected && parseInt(selected.value) === q.correct) {
      score++;
    }
  });  
  // 1. Убираем все вопросы с экрана
  quizContainer.innerHTML = ""; 
  // 2. Скрываем кнопку проверки (найдем её по тегу 'button' или дай ей id='checkBtn')
  // Если у тебя одна кнопка на странице:
  document.querySelector('button').style.display = 'none';
  // 3. Выводим результат
  document.getElementById('result').innerHTML =
    `<h2 style="text-align: center; margin-top: 50px;">
        Ваш результат: ${score} из ${questions.length}
     </h2>`;
}